class Пицца:
    """
    Класс Пицца содержит общие атрибуты для пиццы.

    Атрибуты:
        название (str): Название пиццы.
        тесто (str): Тип теста (тонкое или пышное).
        соус (str): Тип соуса.
        начинка (list): Список начинок.
        цена (float): Цена пиццы.

    Методы:
        __init__: Инициализирует объект Пицца.
        __str__: Возвращает информацию о пицце.
        подготовить: Сообщает о процессе подготовки пиццы.
        испечь: Сообщает о процессе запекания пиццы.
        нарезать: Сообщает о процессе нарезки пиццы.
        упаковать: Сообщает о процессе упаковки пиццы.
    """

    def __init__(self):
        """
        Инициализирует объект Пицца.

        Устанавливает значения по умолчанию для атрибутов:
        - название: "Заготовка"
        - тесто: "тонкое"
        - соус: "кетчуп"
        - начинка: []
        - цена: 0
        """
        self.название = "Заготовка"
        self.тесто = "тонкое"
        self.соус = "кетчуп"
        self.начинка = []
        self.цена = 0

    def __str__(self):
        """
        Возвращает информацию о пицце.

        Возвращает:
            str: Информация о пицце в формате:
                 "Пицца: <название> | Цена: <цена> р.
                  Тесто: <тесто> Соус: <соус>
                  Начинка: <начинка>"
        """
        return (f"Пицца: {self.название} | Цена: {self.цена:.2f} р.\n"
                f"Тесто: {self.тесто} Соус: {self.соус}\n"
                f"Начинка: {', '.join(self.начинка)}")

    def подготовить(self):
        """
        Сообщает о процессе подготовки пиццы.

        Выводит сообщения:
        - Начинаю готовить пиццу <название>.
        - Замешиваю <тесто> тесто...
        - Добавляю соус: <соус>...
        - И, конечно: <начинка>...
        """
        print(f"Начинаю готовить пиццу {self.название}")
        print(f"  - замешиваю {self.тесто} тесто...")
        print(f"  - добавляю соус: {self.соус}...")
        print(f"  - и, конечно: {', '.join(self.начинка)}...")

    def испечь(self):
        """
        Сообщает о процессе запекания пиццы.

        Выводит сообщение: "Выпекаю пиццу... Готово!"
        """
        print("Выпекаю пиццу... Готово!")

    def нарезать(self):
        """
        Сообщает о процессе нарезки пиццы.

        Выводит сообщение: "Нарезаю на аппетитные кусочки..."
        """
        print("Нарезаю на аппетитные кусочки...")

    def упаковать(self):
        """
        Сообщает о процессе упаковки пиццы.

        Выводит сообщение: "Упаковываю в фирменную упаковку и готово!"
        """
        print("Упаковываю в фирменную упаковку и готово!")


